# int - целые
# float - вещественные
# complex - комплексные

# abs - модуль
print(abs(5))
print(abs(-5))

# побитовые операции
print(4 & 6)  # побитовое И
print(4 | 6)  # побитовое ИЛИ
print(4 ^ 6)  # побитовое исключающее ИЛИ

print(bin(4))  # 0100
print(bin(6))  # 0110
# побитовое И    0100
# побитовое ИЛИ  0110
# исключающее ИЛИ 0010

print(oct(4))  # восьмеричный формат
print(hex(4))  # шестнадцатеричный формат

print(float(4))

# комплексные числа
# a + ib -- 3 + 4i
print(complex(5, 6))
